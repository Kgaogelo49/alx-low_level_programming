#include "main.h"

/**
 * print_diagonal - printing lins diagonal
 * @n: input variable
 *
 * Return: void
 */

vid print_diagonal(int n)
{
	int i, j;

	if (n <= 0)
	{
		_putchar('\n');
	}
	for (i = 0; i < n; n++)
	{
		for (j = 0; j < i; j++)
		{
			_putchar(' ');
		}
		_putchar('\\');
		_putchar('\n');
	}
}
