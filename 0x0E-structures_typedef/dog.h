#ifndef DOG_H
#define DOG_H

/**
 * struct dog_t - Represents information about a dog
 * @name: The name of the dog
 * @age: The age of the dog
 * @owner: The owner of the dog
 *
 * This structure holds data about a dog, its name, age, and owner.
 */
struct dog_t
{
	char *name;
	float age;
	char *owner;
};
#endif /* DOG_H */
